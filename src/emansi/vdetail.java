    public void setPenyelenggara() {
        String opsiAcara = cbAcara.getSelectedItem().toString();
        int id = connection.getID(opsiAcara);
        try {
            Statement st = connection.getKoneksi().createStatement();
            ResultSet rs = st.executeQuery("select l.username from Event e "
                    + "join login l on e.id_user = l.id_user "
                    + "where e.id_event = " + id);
            rs.next();
            Lpenyelenggara.setText(rs.getString(1));
        } catch (SQLException e) {
            System.out.println("" + e);
        }
    }

    public void setDetail(String isi, JLabel label) {
        String opsiAcara = cbAcara.getSelectedItem().toString();
        int id = connection.getID(opsiAcara);
        System.out.println(opsiAcara);
        try {
            Statement st = connection.getKoneksi().createStatement();
            ResultSet rs = st.executeQuery("SELECT " + isi + " from Event "
                    + "where id_event = " + id);
            rs.next();
            label.setText(rs.getString(1));
        } catch (SQLException e) {
            System.out.println("" + e);
        }
    }
    
    public void setTotal() {
        String opsiAcara = cbAcara.getSelectedItem().toString();
        int id = connection.getID(opsiAcara);
        try {
            Statement st = connection.getKoneksi().createStatement();
            ResultSet rs = st.executeQuery("select e.nama_event, count(*) as Total_Peserta "
                    + "from data d join Event e on d.id_event = e.id_event "
                    + "join login l on e.id_user = l.id_user "
                    + "where e.id_event = "+id +""
                    + " group by e.nama_event");
             rs.last();
             int count = rs.getRow();
             rs.beforeFirst();
             rs.next();
             Ltotal.setText((count > 0)?rs.getString(2):"0");
            
        } catch (SQLException e) {
            System.out.println("" + e);
        }
    }

