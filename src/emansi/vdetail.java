    public void showStatus(String status, JTable tabel) {
        String query = "select e.nama_event, count(*) as Total_Peserta "
                + "from data d join Event e "
                + "on d.id_event = e.id_event "
                + "join login l on e.id_user = l.id_user "
                + "where l.id_user = '" + usr.get() + "' and d.id_jenis = '" + status + "' "
                + "group by e.nama_event";
        DefaultTableModel model = connection.select(query);
        tabel.setModel(model);
    }

    public void getAcara() {
        String query = "SELECT * From Event e "
                + "left join login l on e.id_user = l.id_user "
                + "where e.id_user='" + usr.get() + "'";
        String j[] = connection.getData(query, 2);
        for (int i = 0; i < j.length; i++) {
            cbAcara.addItem(j[i]);
        }
    }

